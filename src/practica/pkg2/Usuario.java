package practica.pkg2;

public class Usuario {
    private String nombre;
    private String cont;


    public Usuario() {

    }

    public String getNombre() {
        return nombre;
    }

    public String getCont() {
        return cont;
    }

    public void setNombre(String nom) {
        this.nombre = nom;
    }

    public void setCont(String contras) {
        this.cont = contras;
    }
    
}
